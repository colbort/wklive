func RandomNickname() (string, error) {
	nickname, err := randomNicknamePart(nicknames)
	if err != nil {
		return "", err
	}
	suffix, err := rand.Int(rand.Reader, big.NewInt(9000))
	if err != nil {
		return "", err
	}
	return fmt.Sprintf("%s%d", nickname, suffix.Int64()+1000), nil
}

func randomNicknamePart(items []string) (string, error) {
	n, err := rand.Int(rand.Reader, big.NewInt(int64(len(items))))
	if err != nil {
		return "", err
	}
	return items[n.Int64()], nil
}
